package pe.edu.unsa.epcc.dbp;

public class person {
	
	private String name;
	private String age;
	 
	public person () {
		
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	 
	 
} 
